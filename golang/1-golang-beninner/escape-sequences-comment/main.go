func main() {
	fmt.Println("My name is \n uzzal kumar roy")
	fmt.Println("I am a software engineer")
	fmt.Print("I am learning golang\n")
	fmt.Println("Uzzal Roy has got a new job")

	fmt.Println("Name \t Age \t City")
	fmt.Println("Uzzal \t 30 \t New York")
	fmt.Println("Nasir \t 25 \t San Francisco")

	fmt.Println("Uzzal Kumar Roy said, \"I am excited to learn golang!\"")
}
